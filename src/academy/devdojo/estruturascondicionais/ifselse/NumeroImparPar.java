package academy.devdojo.estruturascondicionais.ifselse;

public class NumeroImparPar {
    public static void main(String[] args) {
        float numeroImparPar = 3F;
        if(numeroImparPar % 2 == 0) {
            System.out.println("O número " + numeroImparPar + " é par");
        } else {
        System.out.println("O número "+numeroImparPar+" é impar");
        }
    }
}

/*
dado um determinado número inteiro
imprima se ele é impar ou par
 */